#!/usr/bin/node

import { createHash } from "node:crypto";
import {
  closeSync,
  constants,
  fstatSync,
  lstatSync,
  openSync,
  readFileSync,
} from "node:fs";
import { isDeepStrictEqual } from "node:util";

const LANGUAGES = Object.freeze(["c", "cpp", "java", "python", "javascript"]);
const SHA256 = /^[a-f0-9]{64}$/;
const OCI_DIGEST = /^sha256:[a-f0-9]{64}$/;
const IMMUTABLE_REFERENCE = /^[a-z0-9][a-z0-9./_-]{0,255}@sha256:[a-f0-9]{64}$/;
const MAX_RECORD_BYTES = 65_536;

function fail(message) {
  throw new Error(message);
}

function sameIdentity(left, right) {
  return left.dev === right.dev
    && left.ino === right.ino
    && left.size === right.size
    && left.mtimeMs === right.mtimeMs
    && left.ctimeMs === right.ctimeMs;
}

function readSafeRootFile(file) {
  if (typeof file !== "string" || !file.startsWith("/") || file.includes("\0")) {
    fail("runtime record path must be absolute and NUL-free");
  }
  const before = lstatSync(file);
  if (
    !before.isFile()
    || before.isSymbolicLink()
    || before.uid !== 0
    || before.gid !== 0
    || before.nlink !== 1
    || (before.mode & 0o022) !== 0
    || before.size < 1
    || before.size > MAX_RECORD_BYTES
  ) {
    fail("runtime record file metadata is unsafe");
  }

  const descriptor = openSync(file, constants.O_RDONLY | constants.O_NOFOLLOW);
  try {
    const opened = fstatSync(descriptor);
    if (!opened.isFile() || !sameIdentity(before, opened)) {
      fail("runtime record changed before descriptor binding");
    }
    const bytes = readFileSync(descriptor);
    const after = fstatSync(descriptor);
    if (!sameIdentity(opened, after) || bytes.length !== opened.size) {
      fail("runtime record changed while it was read");
    }
    return new TextDecoder("utf-8", { fatal: true }).decode(bytes);
  } finally {
    closeSync(descriptor);
  }
}

function hasExactKeys(value, expected) {
  if (value === null || typeof value !== "object" || Array.isArray(value)) return false;
  const actual = Object.keys(value).sort();
  const wanted = [...expected].sort();
  return actual.length === wanted.length && actual.every((key, index) => key === wanted[index]);
}

function canonicalRecord(jsonText) {
  let document;
  try {
    document = JSON.parse(jsonText);
  } catch {
    fail("runtime image record JSON is malformed");
  }
  if (
    !hasExactKeys(document, ["schemaVersion", "recordId", "release", "local", "records"])
    || document.schemaVersion !== 1
    || typeof document.recordId !== "string"
    || !SHA256.test(document.recordId)
    || typeof document.release !== "string"
    || document.release.length < 1
    || typeof document.local !== "boolean"
    || !Array.isArray(document.records)
    || document.records.length !== LANGUAGES.length
  ) {
    fail("runtime image record has a non-canonical top-level schema");
  }

  const records = document.records.map((record, index) => {
    if (
      !hasExactKeys(record, ["language", "reference", "manifestDigest", "configDigest", "rootDigest"])
      || record.language !== LANGUAGES[index]
      || typeof record.reference !== "string"
      || !IMMUTABLE_REFERENCE.test(record.reference)
      || typeof record.manifestDigest !== "string"
      || !OCI_DIGEST.test(record.manifestDigest)
      || !record.reference.endsWith(`@${record.manifestDigest}`)
      || typeof record.configDigest !== "string"
      || !OCI_DIGEST.test(record.configDigest)
      || record.configDigest === record.manifestDigest
      || typeof record.rootDigest !== "string"
      || !OCI_DIGEST.test(record.rootDigest)
      || (document.local && record.rootDigest !== record.manifestDigest)
    ) {
      fail("runtime image record contains a non-canonical image identity");
    }
    return {
      language: record.language,
      reference: record.reference,
      manifestDigest: record.manifestDigest,
      configDigest: record.configDigest,
      rootDigest: record.rootDigest,
    };
  });

  const payload = {
    schemaVersion: 1,
    release: document.release,
    local: document.local,
    records,
  };
  const recordId = createHash("sha256").update(JSON.stringify(payload)).digest("hex");
  const expectedDocument = { schemaVersion: 1, recordId, ...payload };
  if (recordId !== document.recordId || !isDeepStrictEqual(expectedDocument, document)) {
    fail("runtime image record does not match its canonical record id");
  }
  const envText = `${[
    "# Generated by runtime/manage-images.mjs record; do not hand-edit.",
    `# runtime-record-id=${recordId}`,
    ...records.map((record) => `RUNNER_IMAGE_${record.language.toUpperCase()}=${record.reference}`),
  ].join("\n")}\n`;
  return { recordId, envText };
}

function main() {
  if (process.platform !== "linux" || process.getuid?.() !== 0 || process.geteuid?.() !== 0) {
    fail("runtime record verification requires real Linux root");
  }
  if (process.argv.length !== 4) {
    fail("usage: verify-runtime-record.mjs JSON_RECORD ENV_PROJECTION");
  }
  const [jsonFile, envFile] = process.argv.slice(2);
  const firstJson = readSafeRootFile(jsonFile);
  const firstEnv = readSafeRootFile(envFile);
  const canonical = canonicalRecord(firstJson);
  if (firstEnv !== canonical.envText) {
    fail("runtime image environment projection does not match its canonical record id");
  }

  // JSON is the transaction commit marker. A second exact read of both files
  // rejects a publication that changed while the verifier crossed the pair.
  if (readSafeRootFile(jsonFile) !== firstJson || readSafeRootFile(envFile) !== firstEnv) {
    fail("runtime image record changed during pair verification");
  }
  process.stdout.write(`runtime-record-id=${canonical.recordId}\n`);
}

try {
  main();
} catch (error) {
  const message = error instanceof Error ? error.message : "unknown verification error";
  process.stderr.write(`runtime record verification failed: ${message}\n`);
  process.exitCode = 1;
}
